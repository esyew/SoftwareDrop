public class Tire {
private int psi;
/**
 * Accepts any int, however must be above 32 to start engine.
 * @param psi what is each Tire's PSI?
 */
public Tire(int psi) {
	super();
	this.psi = psi;
}

/**
 * 
 * @return called tire's PSI Value
 */
public int getPsi() {
	return psi;
}




}
